#include "ft_printf.h"

int	ft_dec(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		ft_char('-');
		ft_char('2');
		n = 147483648;
		i = 2;
	}
	if (n < 0)
	{
		i += ft_char('-');
		n *= -1;
	}
	ft_putnbr_base(n, "0123456789");
	if (n == 0)
		i++;
	i += n_sp(n, 10);
	return (i);
}
